-- 显示行数	
vim.opt.nu = true 
 
-- 相对行显示
vim.opt.relativenumber = true 


-- 设置缩进宽度为 4 个空格
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true -- 将 Tab 键转换为空格

-- 可选：设置自动缩进
vim.opt.autoindent = true
vim.opt.smartindent = true

vim.opt.clipboard = "unnamedplus" -- 让普通的 y 复制到系统剪贴板

vim.opt.foldenable = false
